		static void Problem2(string[] lines)
		{
			int trees1 = CountTreesSlope(lines, 1, 1);
			Console.WriteLine("#2 Trees1: " + trees1);
			int trees2 = CountTreesSlope(lines, 3, 1);
			Console.WriteLine("#2 Trees2: " + trees2);
			int trees3 = CountTreesSlope(lines, 5, 1);
			Console.WriteLine("#2 Trees3: " + trees3);
			int trees4 = CountTreesSlope(lines, 7, 1);
			Console.WriteLine("#2 Trees4: " + trees4);
			int trees5 = CountTreesSlope(lines, 1, 2);
			Console.WriteLine("#2 Trees5: " + trees5);
			Console.WriteLine("#2 Trees Multiplied: " + (long)trees1 * trees2 * trees3 * trees4 * trees5);
		}

		static int CountTreesSlope(string[] lines, int slopeX, int slopeY)
		{
			int trees = 0;
			for (int x = slopeX, y = slopeY; lines.Length > y; y += slopeY, x += slopeX)
			{
				if ('#' == lines[y][x % lines[y].Length])
					++trees;
			}

			return trees;
		}

